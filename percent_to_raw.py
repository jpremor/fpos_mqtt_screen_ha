def correlate_percent(percent=None, raw=None):
    if percent is not None:
        for fst, snd in twofithyfive_to_hundred:
            if fst == percent:
                return snd
    elif raw is not None:
        for first, sec in twofithyfive_to_hundred:
            if sec == raw:
                return first
    else:
        raise ValueError("Either first or second must be provided")